from __future__ import annotations

import os
import re
import subprocess
import threading

import core.state as s
from core.interpreter import project_python
from core.process import proj_run, program_name, program_version


def run_capture(cmd: list[str], timeout: int = s._BUILD_TIMEOUT) -> tuple[int, str]:
    try:
        out = subprocess.run(cmd, cwd=s._HERE, capture_output=True, text=True,
                             timeout=timeout, creationflags=s._NO_WINDOW)
        return out.returncode, (out.stdout + out.stderr)
    except (OSError, subprocess.SubprocessError) as e:
        return 1, str(e)


def iscc_path() -> str | None:
    if os.path.exists(s._ISCC_DEFAULT):
        return s._ISCC_DEFAULT
    import shutil
    return shutil.which("ISCC")


